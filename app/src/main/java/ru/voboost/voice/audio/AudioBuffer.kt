package ru.voboost.voice.audio

import java.util.concurrent.locks.ReentrantLock
import kotlin.concurrent.withLock
import kotlin.math.min

/**
 * в•ЁРҙв•Ёв••в•Ёв•‘в•ӨР‘в•Ёв••в•ӨРҗв•Ёв•ӣв•Ёв–“в•Ёв–‘в•Ёв•ңв•Ёв•ңв•ӨРӣв•Ёв•Ј в•Ёв•‘в•Ёв•ӣв•Ёв•—в•ӨРңв•ӨР–в•Ёв•Ўв•Ёв–“в•Ёв•ӣв•Ёв•Ј в•Ёв–’в•ӨР“в•ӨР”в•Ёв•Ўв•ӨРҗ в•Ёв”Өв•Ёв•—в•ӨРҹ в•Ёв–‘в•ӨР“в•Ёв”Өв•Ёв••в•Ёв•ӣ + в•Ёв”Өв•Ёв•Ўв•ӨР’в•Ёв•Ўв•Ёв•‘в•ӨР’в•Ёв•ӣв•ӨРҗ в•ӨР’в•Ёв••в•ӨРҳв•Ёв••в•Ёв•ңв•ӨРӣ.
 *
 * в•ЁР®в•ӨР‘в•Ёв•ӣв•Ёв–’в•Ёв•Ўв•Ёв•ңв•Ёв•ңв•Ёв•ӣв•ӨР‘в•ӨР’в•Ёв••:
 * - СӮРӯРң в•ЁРӯв•Ёв•Ўв•ӨР’ в•Ёв–‘в•Ёв•—в•Ёв•—в•Ёв•ӣв•Ёв•‘в•Ёв–‘в•ӨР–в•Ёв••в•Ёв•Ј в•Ёв–“ hot path (в•ӨР”в•Ёв••в•Ёв•‘в•ӨР‘в•Ёв••в•ӨРҗв•Ёв•ӣв•Ёв–“в•Ёв–‘в•Ёв•ңв•Ёв•ңв•ӨРӣв•Ёв•Ј в•ӨРҗв•Ёв–‘в•Ёв•–в•Ёв•қв•Ёв•Ўв•ӨРҗ)
 * - СӮРӯРң в•ЁРӯв•Ёв•Ўв•ӨР’ в•Ёв”Өв•Ёв••в•Ёв•ңв•Ёв–‘в•Ёв•қв•Ёв••в•ӨР—в•Ёв•Ўв•ӨР‘в•Ёв•‘в•Ёв•ӣв•Ёв”Ӯв•Ёв•ӣ в•ӨРҗв•Ёв–‘в•ӨР‘в•ӨРҳв•Ёв••в•ӨРҗв•Ёв•Ўв•Ёв•ңв•Ёв••в•ӨРҹ (в•Ёв”җв•ӨРҗв•Ёв•Ўв•Ёв”Өв•ӨР‘в•Ёв•‘в•Ёв–‘в•Ёв•–в•ӨР“в•Ёв•Ўв•Ёв•қв•Ёв–‘в•ӨРҹ в•Ёв”җв•Ёв–‘в•Ёв•қв•ӨРҹв•ӨР’в•ӨРң)
 * - СӮР¬Р• в•ЁРўв•ӨР‘в•ӨР’в•ӨРҗв•Ёв•ӣв•Ёв•Ўв•Ёв•ңв•Ёв•ңв•ӨРӣв•Ёв•Ј RMS-based silence detection
 * - СӮР¬Р• Thread-safe в•ӨР—в•Ёв•Ўв•ӨРҗв•Ёв•Ўв•Ёв•– ReentrantLock (в•Ёв–’в•ӨРӣв•ӨР‘в•ӨР’в•ӨРҗв•Ёв•Ўв•Ёв•Ў synchronized в•Ёв”Өв•Ёв•—в•ӨРҹ в•ӨР—в•Ёв–‘в•ӨР‘в•ӨР’в•ӨРӣв•ӨР• в•Ёв•ӣв•Ёв”җв•Ёв•Ўв•ӨРҗв•Ёв–‘в•ӨР–в•Ёв••в•Ёв•Ј)
 */
class AudioBuffer(
    val capacityBytes: Int = 256 * 1024, // 256KB ~ 8 в•ӨР‘в•Ёв•Ўв•Ёв•‘ @16kHz/16bit mono
    private val sampleRate: Int = 16000,
    private val bitsPerSample: Int = 16,
    // в•ЁРҜв•Ёв–‘в•ӨРҗв•Ёв–‘в•Ёв•қв•Ёв•Ўв•ӨР’в•ӨРҗв•ӨРӣ в•Ёв”Өв•Ёв•Ўв•ӨР’в•Ёв•Ўв•Ёв•‘в•ӨР’в•Ёв•ӣв•ӨРҗв•Ёв–‘ в•ӨР’в•Ёв••в•ӨРҳв•Ёв••в•Ёв•ңв•ӨРӣ
    private var silenceThresholdDb: Float = -45f, // dBFS, в•ӨР’в•Ёв••в•Ёв”җв•Ёв••в•ӨР—в•Ёв•ңв•Ёв•ӣ -50..-40
    private var minSilenceDurationMs: Int = 600,  // в•ӨР‘в•Ёв•‘в•Ёв•ӣв•Ёв•—в•ӨРңв•Ёв•‘в•Ёв•ӣ в•ӨР’в•Ёв••в•ӨРҳв•Ёв••в•Ёв•ңв•ӨРӣ в•ӨР‘в•ӨР—в•Ёв••в•ӨР’в•Ёв–‘в•ӨР’в•ӨРң в•Ёв•‘в•Ёв•ӣв•Ёв•ңв•ӨР–в•Ёв•ӣв•Ёв•қ в•ӨР”в•ӨРҗв•Ёв–‘в•Ёв•–в•ӨРӣ
    private val rmsWindowSizeMs: Int = 50         // в•Ёв•ӣв•Ёв•‘в•Ёв•ңв•Ёв•ӣ в•Ёв”Өв•Ёв•—в•ӨРҹ в•ӨРҗв•Ёв–‘в•ӨР‘в•ӨР—в•ӨРЎв•ӨР’в•Ёв–‘ RMS
                 ) {
    companion object {
        const val TAG = "AudioBuffer"
        private val LOCK = ReentrantLock()
    }

    private val buffer = ByteArray(capacityBytes)
    private var head: Int = 0 // в•Ёв•ӣв•ӨР’в•Ёв•‘в•ӨР“в•Ёв”Өв•Ёв–‘ в•ӨР—в•Ёв••в•ӨР’в•Ёв–‘в•ӨР’в•ӨРң
    private var tail: Int = 0 // в•Ёв•‘в•ӨР“в•Ёв”Өв•Ёв–‘ в•Ёв”җв•Ёв••в•ӨР‘в•Ёв–‘в•ӨР’в•ӨРң
    private var size: Int = 0 // в•ӨР‘в•Ёв•‘в•Ёв•ӣв•Ёв•—в•ӨРңв•Ёв•‘в•Ёв•ӣ в•Ёв”Өв•Ёв–‘в•Ёв•ңв•Ёв•ңв•ӨРӣв•ӨР• в•Ёв”Өв•Ёв•ӣв•ӨР‘в•ӨР’в•ӨР“в•Ёв”җв•Ёв•ңв•Ёв•ӣ

    // в•ЁРұв•Ёв•ӣв•ӨР‘в•ӨР’в•Ёв•ӣв•ӨРҹв•Ёв•ңв•Ёв••в•Ёв•Ў в•Ёв”Өв•Ёв•Ўв•ӨР’в•Ёв•Ўв•Ёв•‘в•ӨР’в•Ёв•ӣв•ӨРҗв•Ёв–‘ в•ӨР’в•Ёв••в•ӨРҳв•Ёв••в•Ёв•ңв•ӨРӣ
    private var lastSpeechTimeMs: Long = 0
    private var rmsSum: Long = 0
    private var rmsCount: Int = 0
    private val rmsWindowSamples: Int = (sampleRate * rmsWindowSizeMs) / 1000

    // в•ЁРӘв•Ёв•ӣв•Ёв•ңв•Ёв–“в•Ёв•Ўв•ӨРҗв•ӨР‘в•Ёв••в•ӨРҹ в•Ёв”җв•Ёв•ӣв•ӨРҗв•Ёв•ӣв•Ёв”Ӯв•Ёв–‘: dBFS СӮР–Рў в•Ёв•—в•Ёв••в•Ёв•ңв•Ёв•Ўв•Ёв•Јв•Ёв•ңв•Ёв–‘в•ӨРҹ в•Ёв–‘в•Ёв•қв•Ёв”җв•Ёв•—в•Ёв••в•ӨР’в•ӨР“в•Ёв”Өв•Ёв–‘ (в•Ёв”Өв•Ёв•—в•ӨРҹ 16-bit PCM)
    private val silenceThresholdLinear =
            (Short.MAX_VALUE * Math.pow(10.0, silenceThresholdDb / 20.0)).toInt()

    /**
     * в•ЁРӨв•Ёв•ӣв•Ёв–’в•Ёв–‘в•Ёв–“в•Ёв••в•ӨР’в•ӨРң в•Ёв–‘в•ӨР“в•Ёв”Өв•Ёв••в•Ёв•ӣ-в•Ёв”Өв•Ёв–‘в•Ёв•ңв•Ёв•ңв•ӨРӣв•Ёв•Ў. в•ЁРўв•Ёв•ӣв•Ёв•–в•Ёв–“в•ӨРҗв•Ёв–‘в•ӨРҷв•Ёв–‘в•Ёв•Ўв•ӨР’ true, в•Ёв•Ўв•ӨР‘в•Ёв•—в•Ёв•• в•Ёв•ӣв•Ёв–’в•Ёв•ңв•Ёв–‘в•ӨРҗв•ӨР“в•Ёв•ўв•Ёв•Ўв•Ёв•ң в•Ёв•‘в•Ёв•ӣв•Ёв•ңв•Ёв•Ўв•ӨР– в•ӨР”в•ӨРҗв•Ёв–‘в•Ёв•–в•ӨРӣ (в•ӨР’в•Ёв••в•ӨРҳв•Ёв••в•Ёв•ңв•Ёв–‘).
     *
     * СӮРӘР°СҸв••Рҹ в•ЁРўв•ӨРӣв•Ёв•–в•ӨРӣв•Ёв–“в•Ёв–‘в•Ёв•Ўв•ӨР’в•ӨР‘в•ӨРҹ в•Ёв••в•Ёв•– в•Ёв–‘в•ӨР“в•Ёв”Өв•Ёв••в•Ёв•ӣ-в•Ёв•‘в•Ёв•ӣв•Ёв•—в•Ёв•—в•Ёв–’в•ӨРқв•Ёв•‘в•Ёв–‘ СӮРҗРӨ в•ЁРӯв•ЁРҘ в•ЁРӨв•ЁР®в•ЁР«в•ЁРҰв•ЁРҘв•ЁРӯ в•Ёв–‘в•Ёв•—в•Ёв•—в•Ёв•ӣв•ӨР–в•Ёв••в•ӨРҗв•Ёв•ӣв•Ёв–“в•Ёв–‘в•ӨР’в•ӨРң!
     */
    fun put(data: ByteArray, offset: Int, length: Int): Boolean {
        LOCK.lock()
        try {
            // 1. в•ЁР§в•Ёв–‘в•Ёв”җв•Ёв••в•ӨР‘в•ӨРӣв•Ёв–“в•Ёв–‘в•Ёв•Ўв•Ёв•қ в•Ёв”Өв•Ёв–‘в•Ёв•ңв•Ёв•ңв•ӨРӣв•Ёв•Ў в•Ёв–“ в•Ёв•‘в•Ёв•ӣв•Ёв•—в•ӨРңв•ӨР–в•Ёв•Ўв•Ёв–“в•Ёв•ӣв•Ёв•Ј в•Ёв–’в•ӨР“в•ӨР”в•Ёв•Ўв•ӨРҗ
            var written = 0
            while (written < length) {
                val space = capacityBytes - tail
                val toWrite = minOf(length - written, space)
                System.arraycopy(data, offset + written, buffer, tail, toWrite)
                tail = (tail + toWrite) and (capacityBytes - 1) // в•Ёв–’в•ӨРӣв•ӨР‘в•ӨР’в•ӨРҗв•ӨРӣв•Ёв•Ј mod в•Ёв”Өв•Ёв•—в•ӨРҹ в•ӨР‘в•ӨР’в•Ёв•Ўв•Ёв”җв•Ёв•Ўв•Ёв•ңв•Ёв•• в•Ёв”Өв•Ёв–“в•Ёв•ӣв•Ёв•Јв•Ёв•‘в•Ёв••
                size = min(size + toWrite, capacityBytes)
                written += toWrite
            }

            // 2. в•ЁР®в•Ёв–’в•Ёв•ңв•Ёв•ӣв•Ёв–“в•Ёв•—в•ӨРҹв•Ёв•Ўв•Ёв•қ в•Ёв”Өв•Ёв•Ўв•ӨР’в•Ёв•Ўв•Ёв•‘в•ӨР’в•Ёв•ӣв•ӨРҗ в•ӨР’в•Ёв••в•ӨРҳв•Ёв••в•Ёв•ңв•ӨРӣ
            val now = System.currentTimeMillis()
            if (hasSpeechActivity(data, offset, length)) {
                lastSpeechTimeMs = now
            }

            // 3. в•ЁРҜв•ӨРҗв•Ёв•ӣв•Ёв–“в•Ёв•Ўв•ӨРҗв•ӨРҹв•Ёв•Ўв•Ёв•қ, в•Ёв•ңв•Ёв•Ў в•Ёв”җв•ӨРҗв•Ёв•ӣв•ӨРҳв•Ёв•—в•Ёв–‘ в•Ёв•—в•Ёв•• в•ӨР’в•Ёв••в•ӨРҳв•Ёв••в•Ёв•ңв•Ёв–‘ в•Ёв”Өв•Ёв•ӣв•Ёв•—в•ӨРңв•ӨРҳв•Ёв•Ў в•Ёв”җв•Ёв•ӣв•ӨРҗв•Ёв•ӣв•Ёв”Ӯв•Ёв–‘
            val silenceDuration = now - lastSpeechTimeMs
            return silenceDuration >= minSilenceDurationMs && size > 0

        } finally {
            LOCK.unlock()
        }
    }

    /**
     * в•ЁРҜв•ӨРҗв•Ёв•ӣв•Ёв–“в•Ёв•Ўв•ӨРҗв•Ёв••в•ӨР’в•ӨРң, в•Ёв•Ўв•ӨР‘в•ӨР’в•ӨРң в•Ёв•—в•Ёв•• в•Ёв–‘в•Ёв•‘в•ӨР’в•Ёв••в•Ёв–“в•Ёв•ңв•Ёв•ӣв•ӨР‘в•ӨР’в•ӨРң в•ӨРҗв•Ёв•Ўв•ӨР—в•Ёв•• в•Ёв–“ в•ӨР—в•Ёв–‘в•Ёв•ңв•Ёв•‘в•Ёв•Ў (RMS > в•Ёв”җв•Ёв•ӣв•ӨРҗв•Ёв•ӣв•Ёв”Ӯв•Ёв–‘)
     */
    private fun hasSpeechActivity(data: ByteArray, offset: Int, length: Int): Boolean {
        // в•ЁР°в•Ёв–‘в•Ёв–’в•Ёв•ӣв•ӨР’в•Ёв–‘в•Ёв•Ўв•Ёв•қ в•ӨР’в•Ёв•ӣв•Ёв•—в•ӨРңв•Ёв•‘в•Ёв•ӣ в•ӨР‘ 16-bit PCM (2 в•Ёв–’в•Ёв–‘в•Ёв•Јв•ӨР’в•Ёв–‘ в•Ёв•ңв•Ёв–‘ в•ӨР‘в•ӨРқв•Ёв•қв•Ёв”җв•Ёв•—, little-endian)
        if (bitsPerSample != 16) return true // fallback

        var speechDetected = false
        val end = offset + length - 1

        var i = offset
        while (i < end) {
            // в•ЁР·в•Ёв••в•ӨР’в•Ёв–‘в•Ёв•Ўв•Ёв•қ 16-bit sample (little-endian)
            val sample = (data[i].toInt() and 0xFF) or (data[i + 1].toInt() shl 8)
            val amplitude = kotlin.math.abs(sample)

            if (amplitude > silenceThresholdLinear) {
                speechDetected = true
                break
            }
            i += 2
        }

        return speechDetected
    }

    /**
     * в•ЁРЁв•Ёв•–в•Ёв–“в•Ёв•—в•Ёв•Ўв•ӨР—в•ӨРң в•Ёв”Өв•Ёв–‘в•Ёв•ңв•Ёв•ңв•ӨРӣв•Ёв•Ў в•Ёв”Өв•Ёв•—в•ӨРҹ в•Ёв•ӣв•Ёв–’в•ӨРҗв•Ёв–‘в•Ёв–’в•Ёв•ӣв•ӨР’в•Ёв•‘в•Ёв••. в•ЁРўв•Ёв•ӣв•Ёв•–в•Ёв–“в•ӨРҗв•Ёв–‘в•ӨРҷв•Ёв–‘в•Ёв•Ўв•ӨР’ в•Ёв•‘в•Ёв•ӣв•Ёв•—в•Ёв••в•ӨР—в•Ёв•Ўв•ӨР‘в•ӨР’в•Ёв–“в•Ёв•ӣ в•ӨР‘в•Ёв•‘в•Ёв•ӣв•Ёв”җв•Ёв••в•ӨРҗв•Ёв•ӣв•Ёв–“в•Ёв–‘в•Ёв•ңв•Ёв•ңв•ӨРӣв•ӨР• в•Ёв–’в•Ёв–‘в•Ёв•Јв•ӨР’.
     *
     * @param destination в•Ёв–’в•ӨР“в•ӨР”в•Ёв•Ўв•ӨРҗ в•Ёв”Өв•Ёв•—в•ӨРҹ в•Ёв•‘в•Ёв•ӣв•Ёв”җв•Ёв••в•ӨРҗв•Ёв•ӣв•Ёв–“в•Ёв–‘в•Ёв•ңв•Ёв••в•ӨРҹ (в•Ёв”Өв•Ёв•ӣв•Ёв•—в•Ёв•ўв•Ёв•Ўв•Ёв•ң в•Ёв–’в•ӨРӣв•ӨР’в•ӨРң в•Ёв”Өв•Ёв•ӣв•ӨР‘в•ӨР’в•Ёв–‘в•ӨР’в•Ёв•ӣв•ӨР—в•Ёв•ңв•Ёв•ӣ в•Ёв–’в•Ёв•ӣв•Ёв•—в•ӨРңв•ӨРҳв•Ёв••в•Ёв•қ)
     * @param maxSize в•Ёв•қв•Ёв–‘в•Ёв•‘в•ӨР‘в•Ёв••в•Ёв•қв•Ёв–‘в•Ёв•—в•ӨРңв•Ёв•ңв•Ёв•ӣв•Ёв•Ў в•Ёв•‘в•Ёв•ӣв•Ёв•—в•Ёв••в•ӨР—в•Ёв•Ўв•ӨР‘в•ӨР’в•Ёв–“в•Ёв•ӣ в•Ёв–’в•Ёв–‘в•Ёв•Јв•ӨР’ в•Ёв”Өв•Ёв•—в•ӨРҹ в•Ёв••в•Ёв•–в•Ёв–“в•Ёв•—в•Ёв•Ўв•ӨР—в•Ёв•Ўв•Ёв•ңв•Ёв••в•ӨРҹ (в•Ёв•ӣв•Ёв”җв•ӨР’в•Ёв••в•Ёв•қв•Ёв••в•Ёв•–в•Ёв–‘в•ӨР–в•Ёв••в•ӨРҹ)
     */
    fun take(destination: ByteArray, maxSize: Int = destination.size): Int {
        LOCK.lock()
        try {
            if (size == 0) return 0

            val toCopy = minOf(size, destination.size, maxSize)
            var copied = 0
            var readPos = head

            while (copied < toCopy) {
                val available = capacityBytes - readPos
                val chunk = minOf(toCopy - copied, available)
                System.arraycopy(buffer, readPos, destination, copied, chunk)
                readPos = (readPos + chunk) and (capacityBytes - 1)
                copied += chunk
            }

            head = readPos
            size -= copied
            return copied

        } finally {
            LOCK.unlock()
        }
    }

    /**
     * в•ЁРЎв•ӨРӣв•ӨР‘в•ӨР’в•ӨРҗв•Ёв–‘в•ӨРҹ в•Ёв”җв•ӨРҗв•Ёв•ӣв•Ёв–“в•Ёв•Ўв•ӨРҗв•Ёв•‘в•Ёв–‘: в•Ёв•Ўв•ӨР‘в•ӨР’в•ӨРң в•Ёв•—в•Ёв•• в•Ёв”Өв•Ёв–‘в•Ёв•ңв•Ёв•ңв•ӨРӣв•Ёв•Ў в•Ёв”Өв•Ёв•—в•ӨРҹ в•Ёв•ӣв•Ёв–’в•ӨРҗв•Ёв–‘в•Ёв–’в•Ёв•ӣв•ӨР’в•Ёв•‘в•Ёв••
     */
    fun hasData(minBytes: Int = 1): Boolean = LOCK.withLock { size >= minBytes }

    /**
     * в•ЁРӘв•Ёв•ӣв•Ёв•—в•Ёв••в•ӨР—в•Ёв•Ўв•ӨР‘в•ӨР’в•Ёв–“в•Ёв•ӣ в•Ёв”Өв•Ёв•ӣв•ӨР‘в•ӨР’в•ӨР“в•Ёв”җв•Ёв•ңв•ӨРӣв•ӨР• в•Ёв–’в•Ёв–‘в•Ёв•Јв•ӨР’
     */
    fun availableBytes(): Int = LOCK.withLock { size }

    /**
     * в•ЁРұв•Ёв–’в•ӨРҗв•Ёв•ӣв•ӨР‘в•Ёв••в•ӨР’в•ӨРң в•Ёв–’в•ӨР“в•ӨР”в•Ёв•Ўв•ӨРҗ в•Ёв•• в•Ёв”Өв•Ёв•Ўв•ӨР’в•Ёв•Ўв•Ёв•‘в•ӨР’в•Ёв•ӣв•ӨРҗ в•ӨР’в•Ёв••в•ӨРҳв•Ёв••в•Ёв•ңв•ӨРӣ
     */
    fun reset() {
        LOCK.lock()
        try {
            head = 0
            tail = 0
            size = 0
            lastSpeechTimeMs = 0
            rmsSum = 0
            rmsCount = 0
        } finally {
            LOCK.unlock()
        }
    }

    /**
     * в•ЁРӘв•Ёв•ӣв•Ёв•ңв•ӨР”в•Ёв••в•Ёв”Ӯв•ӨР“в•ӨРҗв•Ёв–‘в•ӨР–в•Ёв••в•ӨРҹ в•Ёв”Өв•Ёв•Ўв•ӨР’в•Ёв•Ўв•Ёв•‘в•ӨР’в•Ёв•ӣв•ӨРҗв•Ёв–‘ в•ӨР’в•Ёв••в•ӨРҳв•Ёв••в•Ёв•ңв•ӨРӣ "в•Ёв•ңв•Ёв–‘ в•Ёв•—в•Ёв•Ўв•ӨР’в•ӨР“"
     */
    fun updateSilenceParams(
        thresholdDb: Float? = null,
        minDurationMs: Int? = null
                           ) {
        LOCK.lock()
        try {
            thresholdDb?.let {
                silenceThresholdDb = it
                // в•ЁРҜв•Ёв•Ўв•ӨРҗв•Ёв•Ўв•ӨР‘в•ӨР—в•Ёв••в•ӨР’в•ӨРӣв•Ёв–“в•Ёв–‘в•Ёв•Ўв•Ёв•қ в•Ёв•—в•Ёв••в•Ёв•ңв•Ёв•Ўв•Ёв•Јв•Ёв•ңв•ӨРӣв•Ёв•Ј в•Ёв”җв•Ёв•ӣв•ӨРҗв•Ёв•ӣв•Ёв”Ӯ
                // (в•Ёв–“ в•ӨРҗв•Ёв•Ўв•Ёв–‘в•Ёв•—в•ӨРңв•Ёв•ңв•Ёв•ӣв•Ёв•қ в•Ёв•‘в•Ёв•ӣв•Ёв”Өв•Ёв•Ў в•Ёв•—в•ӨР“в•ӨР—в•ӨРҳв•Ёв•Ў в•ӨР‘в•Ёв”Өв•Ёв•Ўв•Ёв•—в•Ёв–‘в•ӨР’в•ӨРң в•ӨР‘в•Ёв–“в•Ёв•ӣв•Ёв•Јв•ӨР‘в•ӨР’в•Ёв–“в•Ёв–‘ в•ӨР‘ setter)
            }
            minDurationMs?.let { minSilenceDurationMs = it }
        } finally {
            LOCK.unlock()
        }
    }
