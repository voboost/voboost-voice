package com.voboost.voiceassistant.speech.state

/**
 * Интерфейс состояния голосового помощника
 * 
 * Каждое состояние:
 * 1. Выполняет свою логию
 * 2. Возвращает следующее состояние
 * 
 * Это классический State Machine паттерн:
 * - ✅ SRP: каждое состояние в отдельном классе
 * - ✅ OCP: новые состояния без изменения старых
 * - ✅ Тестируемость: каждое состояние отдельно
 */
interface State {
    /**
     * Выполнить логику состояния
     * @return Следующее состояние
     */
    suspend fun execute(): State
    
    /**
     * Отменить текущее состояние
     * @return Следующее состояние после отмены
     */
    suspend fun cancel(): State = this  // По умолчанию — остаёмся в том же состоянии
    
    /**
     * Активировать помощник (перейти к слушанию команды)
     * @return Следующее состояние
     */
    suspend fun activate(): State = this  // По умолчанию — игнорируем
}
